
public class OperatorDemo {
	
	/*
	 * Arithmetic +,-,*,/,%
	 * Bitwise
	 * Relational
	 * Logical
	 */

	public static void main(String[] args) 
	{
       int m=6, n=4;
       int r1=m+n;   // 10
       int r2=m-n;   // 2
       int r3=m*n;   // 24
       double r4=m/n;   // 
       int r5=m%n; 
       
       System.out.println(r1);
       System.out.println(r2); 
       System.out.println(r3);
       System.out.println(r4);
       System.out.println(r5);
	}

}
